TEST_CASE(test_vreinterpret_f32_s8) {
    struct {
        int8_t a[8];
    } test_vec[] = {
        {{-42, 92, 93, 70, 89, -37, -103, -92}},
        {{40, -95, -44, 67, -63, -92, -108, 10}},
        {{37, 114, -8, -112, -60, -12, -68, -73}},
        {{-40, -11, -75, 51, -124, -99, 109, 90}},
        {{-7, -54, -95, 82, -91, 58, -10, -50}},
        {{-37, -54, 17, -100, 110, -91, -90, -108}},
        {{23, -97, 36, -36, -109, -31, -109, 107}},
        {{-42, 73, -97, 91, -26, 12, -75, -33}},

    };

    for (size_t i = 0; i < (sizeof(test_vec) / sizeof(test_vec[0])); i++) {
        int8x8_t a = vld1_s8(test_vec[i].a);
        float32x2_t b = vreinterpret_f32_s8(a);
        int8x8_t c = vreinterpret_s8_f32(b);
        ASSERT_EQUAL(a, c);
    }

    return 0;
}
